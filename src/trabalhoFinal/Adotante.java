package trabalhoFinal;

public class Adotante extends Usuario {
	private int idade;
	private int espaco;
	private int atividadeFisica;
	private int atividadeFisicaAcompanhado;
	private int tempoDisponivel;
	private int idadeInteresse;
	private int tamanhoInteresse;
	
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	public int getEspaco() {
		return espaco;
	}
	public void setEspaco(int espaco) {
		this.espaco = espaco;
	}
	public int getAtividadeFisica() {
		return atividadeFisica;
	}
	public void setAtividadeFisica(int atividadeFisica) {
		this.atividadeFisica = atividadeFisica;
	}
	public int getAtividadeFisicaAcompanhado() {
		return atividadeFisicaAcompanhado;
	}
	public void setAtividadeFisicaAcompanhado(int atividadeFisicaAcompanhado) {
		this.atividadeFisicaAcompanhado = atividadeFisicaAcompanhado;
	}
	public int getTempoDisponivel() {
		return tempoDisponivel;
	}
	public void setTempoDisponivel(int tempoDisponivel) {
		this.tempoDisponivel = tempoDisponivel;
	}
	public int getIdadeInteresse() {
		return idadeInteresse;
	}
	public void setIdadeInteresse(int idadeInteresse) {
		this.idadeInteresse = idadeInteresse;
	}
	public int getTamanhoInteresse() {
		return tamanhoInteresse;
	}
	public void setTamanhoInteresse(int tamanhoInteresse) {
		this.tamanhoInteresse = tamanhoInteresse;
	}
	
	
}
